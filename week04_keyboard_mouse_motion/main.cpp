#include <GL/glut.h>///使用GLUT外掛 18行
#include <stdio.h>
int N=0;
float teapotX[1000], teapotY[1000];
void display() ///自己寫
{
    glClear(GL_COLOR_BUFFER_BIT|GL_DEPTH_BUFFER_BIT);
    for(int i=0;i<N;i++){
        glPushMatrix();
            glTranslatef(teapotX[i],teapotY[i],0);
            glutSolidTeapot(0.1);
        glPopMatrix();
    }
    glutSwapBuffers();///自己寫
}
void keyboard(unsigned char key, int x,int y){
    printf("key:%c x:%d y:%d\n",key,x,y);
}
void mouse(int button,int state,int x,int y){
    teapotX[N]=(x-150)/150.0;
    teapotY[N]=-(y-150)/150.0;
    N++;
}
int main(int argc, char *argv[])///我們的main函式 138行
{
    glutInit(&argc, argv);///初始化GLUT 140行
    glutInitDisplayMode(GLUT_RGB | GLUT_DOUBLE | GLUT_DEPTH);///143行
    glutCreateWindow("week04_keyboard_mouse_motion");///145行 建視窗
    glutDisplayFunc(display);///148行 設定要畫圖的display函式
    glutKeyboardFunc(keyboard);///鍵盤事件的函式
    glutMouseFunc(mouse);///滑鼠事件的函式
    glutMainLoop();///174行 主要迴圈
}
